    //Relleno la tabla de historial
    private void rellenarTabla(String buscar) {
        DefaultTableModel tableModel = (DefaultTableModel) tblHistorial.getModel();
        tableModel.getDataVector().removeAllElements();
        historial.clear();
        if(buscar.equals("Todo")){
            historial = Conexion.recogeHistorial();
        }else{
            historial = Conexion.recogeHistorial(buscar);
        }
        //Para cambiar el orden del array
        Collections.reverse(historial);
        
        for (int x = 0; x< historial.size();x++){
            //String nombre = materiales.get(x).getNombre();
            String material = historial.get(x).getMaterial();
            String localizacion = historial.get(x).getLocalizacion();
            String fecha = new SimpleDateFormat("yyyy-MM-dd-hh.mm.ss").format(new Date(Long.valueOf(historial.get(x).getFecha())));
            String user = historial.get(x).getUser();
            
            Object[] data = {material,localizacion,fecha,user};
            
            tableModel.addRow(data);

        }
        if(historial.isEmpty()){
            JOptionPane.showMessageDialog(null,"No se ha encontrado datos","Informacion",JOptionPane.INFORMATION_MESSAGE);
            tableModel.getDataVector().removeAllElements();
        }
    }
    
    private void rellenarCombo() {
        cmbBuscar.removeAllItems();
        
        String[] datos = Conexion.getDatos("material");
        for (String dato : datos){
            cmbBuscar.addItem(dato);
        }
        cmbBuscar.addItem("Todo");
    }

