    //Este metodo rellena los comboboxes con los datos de la base de datos
    private void rellenarComboboxes() {
        String[] tipos = Conexion.getDatos("tipo_material");
        String[] proveedores = Conexion.getDatos("proveedor");
        
        for (String tipo : tipos){
            cmbTipo.addItem(tipo);
        }
        
        for (String proveedor : proveedores){
            cmbProveedor.addItem(proveedor);
        }
    }
    

