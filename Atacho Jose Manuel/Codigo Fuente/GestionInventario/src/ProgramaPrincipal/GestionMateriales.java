    private void checkPrecio() {
        //Si el proveedor es 1, lo deshabilito
        if(cmbProveedor.getSelectedItem().toString().substring(0, cmbProveedor.getSelectedItem().toString().indexOf("#")).equals("1")){
            spnPrecio.setEnabled(false);
        }else{
            spnPrecio.setEnabled(true);
        }
    }


