    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        //Boton que agrega un material
        boolean resultado = false;
        if(!(txtNombre.getText().equals("") || txtMarca.getText().equals(""))){
            switch(txtModo.getText()){
                case "Dar de alta":
                    resultado = Conexion.addMaterial(txtNombre.getText(),
                            cmbTipo.getSelectedItem().toString().substring(0, cmbTipo.getSelectedItem().toString().indexOf("#"))
                            ,txtMarca.getText()
                            ,cmbProveedor.getSelectedItem().toString().substring(0, cmbProveedor.getSelectedItem().toString().indexOf("#"))
                            ,"SIN ASIGNAR"
                            ,spnStock.getValue().toString()
                            ,(cmbProveedor.getSelectedItem().toString().substring(0, cmbProveedor.getSelectedItem().toString().indexOf("#")).equals("1")?"0":spnPrecio.getValue().toString()));
                    break;
                case "Modificar":
                    resultado = Conexion.modifMaterial(txtNombre.getText(),
                            cmbTipo.getSelectedItem().toString().substring(0, cmbTipo.getSelectedItem().toString().indexOf("#"))
                            ,txtMarca.getText()
                            ,cmbProveedor.getSelectedItem().toString().substring(0, cmbProveedor.getSelectedItem().toString().indexOf("#"))
                            ,material.getLocalizacion()
                            ,spnStock.getValue().toString()
                            ,(cmbProveedor.getSelectedItem().toString().substring(0, cmbProveedor.getSelectedItem().toString().indexOf("#")).equals("1")?"0":spnPrecio.getValue().toString()));
                    break;
            }
            if(resultado){
                this.dispose();
            }
        }else{
            JOptionPane.showMessageDialog(null,"Rellene todos los datos","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

